const { PrismaClient } = require('@prisma/client');
const axios = require('axios');

const prisma = new PrismaClient();
const API_BASE = 'http://localhost:5000/api';

// Fun√ß√£o para fazer login e obter token
async function login() {
  try {
    const response = await axios.post(`${API_BASE}/auth/login`, {
      email: 'joao@zara.com',
      password: 'password123'
    });
    return response.data.token;
  } catch (error) {
    console.error('‚ùå Erro no login:', error.response?.data || error.message);
    throw error;
  }
}

// Fun√ß√£o para obter dados de produ√ß√£o
async function getProductionData(machineId, token) {
  try {
    const response = await axios.get(`${API_BASE}/machines/${machineId}/production/current-shift`, {
      headers: { Authorization: `Bearer ${token}` }
    });
    return response.data.data;
  } catch (error) {
    console.error('‚ùå Erro ao obter dados de produ√ß√£o:', error.response?.data || error.message);
    return null;
  }
}

// Fun√ß√£o para iniciar opera√ß√£o
async function startOperation(machineId, token) {
  try {
    const response = await axios.post(`${API_BASE}/machines/${machineId}/start-operation`, {
      notes: 'Teste de produ√ß√£o'
    }, {
      headers: { Authorization: `Bearer ${token}` }
    });
    return response.data;
  } catch (error) {
    console.error('‚ùå Erro ao iniciar opera√ß√£o:', error.response?.data || error.message);
    throw error;
  }
}

// Fun√ß√£o para finalizar opera√ß√£o
async function endOperation(machineId, token) {
  try {
    const response = await axios.post(`${API_BASE}/machines/${machineId}/end-operation`, {
      notes: 'Teste finalizado'
    }, {
      headers: { Authorization: `Bearer ${token}` }
    });
    return response.data;
  } catch (error) {
    console.error('‚ùå Erro ao finalizar opera√ß√£o:', error.response?.data || error.message);
    throw error;
  }
}

// Fun√ß√£o para verificar dados da m√°quina
async function getMachineData(machineId) {
  try {
    const machine = await prisma.machine.findUnique({
      where: { id: machineId },
      include: {
        operations: {
          where: { status: 'ACTIVE' },
          include: { user: true }
        },
        shiftData: {
          where: {
            shiftDate: {
              gte: new Date(new Date().setHours(0, 0, 0, 0))
            }
          },
          orderBy: { createdAt: 'desc' },
          take: 1
        }
      }
    });
    return machine;
  } catch (error) {
    console.error('‚ùå Erro ao buscar dados da m√°quina:', error);
    return null;
  }
}

